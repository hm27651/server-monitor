#!/bin/bash
# =============================================================================
# 采集节点配置模板
# =============================================================================
# 部署新采集节点时建议复制本文件：
#   cp config.example.sh config.sh
# 然后按当前服务器实际情况修改 config.sh。
# =============================================================================

# -----------------------------------------------------------------------------
# 本机标识
# -----------------------------------------------------------------------------
NODE_NAME="${NODE_NAME:-$(hostname)}"
NODE_IP="${NODE_IP:-}"

# -----------------------------------------------------------------------------
# 告警阈值配置
# -----------------------------------------------------------------------------
THRESHOLD_CPU="80"
THRESHOLD_MEM="85"
THRESHOLD_DISK="90"
THRESHOLD_NETWORK="100"

# -----------------------------------------------------------------------------
# 监控目标设备配置
# -----------------------------------------------------------------------------
DEV_ETH0="${DEV_ETH0:-eth0}"
DEV_ETH1="${DEV_ETH1:-eth1}"

MONITOR_DEVICES=(
    "${DEV_ETH0}"
    "${DEV_ETH1}"
)

# -----------------------------------------------------------------------------
# 日志与输出配置
# -----------------------------------------------------------------------------
LOG_DIR="${LOG_DIR:-/share/server-monitor/logs}"
DATA_DIR="${DATA_DIR:-/share/server-monitor/data}"
TMP_DIR="${TMP_DIR:-/share/server-monitor/tmp}"

# -----------------------------------------------------------------------------
# 安全配置
# -----------------------------------------------------------------------------
MAX_RETRIES="3"
